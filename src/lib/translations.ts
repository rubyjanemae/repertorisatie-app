/**
 * Zoek vertalingen op basis van een zoekterm
 */
export function searchTranslations(
  query: string,
  direction: 'nl-en' | 'en-nl' | 'both' = 'both'
): TranslationEntry[] {
  const term = query.toLowerCase().trim();
  if (!term) return [];

  return translations.filter(entry => {
    if (direction === 'nl-en' || direction === 'both') {
      if (entry.nl.toLowerCase().includes(term)) return true;
    }
    if (direction === 'en-nl' || direction === 'both') {
      if (entry.en.toLowerCase().includes(term)) return true;
    }
    return false;
  });
}
